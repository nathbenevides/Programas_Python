# 9-Faça um programa que leia um número Inteiro qualquer e mostre na tela a sua tabuada.
n = int(input('Digite um numero para ver sua tabuada: '))
print('Tabuada de {}: \n {}x1= {} \n {}x2= {} \n {}x3= {} \n {}x4= {} \n {}x5= {} \n {}x6= {} \n {}x7= {} \n {}x8= {} \n {}x9= {} \n '
      '{}x10= {}'.format(n, n, n * 1, n, n * 2, n, n * 3, n, n * 4, n, n * 5, n, n * 6, n, n * 7, n, n * 8, n, n * 9, n, n * 10))
